	 *
	 * @param      boolean $deep Whether to also clear the references on all associated objects.
	 */
	public function clearAllReferences($deep = false)
	{
		if ($deep) {
			if ($this->collImageBoxs) {
				foreach ((array) $this->collImageBoxs as $o) {
					$o->clearAllReferences($deep);
				}
			}
			if ($this->collTextBoxs) {
				foreach ((array) $this->collTextBoxs as $o) {
					$o->clearAllReferences($deep);
				}
			}
		} // if ($deep)

		$this->collImageBoxs = null;
		$this->collTextBoxs = null;
	}

